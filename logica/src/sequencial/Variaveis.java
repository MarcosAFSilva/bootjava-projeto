package sequencial;

public class Variaveis {

	public static void main(String[] args) {

		int quantidade = 1500;
		float valorMercadoria = 750.28f;
		double peso = 78.3;
		char genero = 'F';
		
		System.out.printf("A quantidade de itens em estoque é %d\n", quantidade);
		System.out.printf("O valor da parcela é R$ %.2f\n", valorMercadoria);
		System.out.printf("O peso do produto é %f kg\n", peso);
		System.out.printf("Existem %d alunas matriculadas do gênero %c\n", quantidade, genero);
	}

}
